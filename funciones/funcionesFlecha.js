// Funciones flecha 

const sumarFuncionFlecha = (a, b) => a + b;

// Para una funcion flecha ya no es necesario utilizar la palabra function 
// Los corchetes donde va el cuerpo de la funcion serán remplazados por "=>"
// La palabra return ya no será necesaria

let resultado = sumarFuncionFlecha(12, 21);
console.log(resultado);
